# prompt: Write the code simple Zakat calculator

def zakat_calculator():
    """Calculates Zakat on savings."""

    try:
        # Get the savings amount from the user
        savings = float(input("Enter your total savings amount: "))

        # Check if the savings amount is greater than or equal to the Nisab
        nisab = 85 # This is the approximate Nisab value in USD. It can vary.
        if savings >= nisab:
            # Calculate Zakat
            zakat = savings * (2.5 / 100)
            print(f"Your Zakat amount is: ${zakat:.2f}")
        else:
            print("Your savings do not meet the Nisab requirement for Zakat.")

    except ValueError:
        print("Invalid input. Please enter a numeric value for your savings.")

# Call the function to start the calculator
zakat_calculator()
